function calcular (x1, x2, operador) {
  switch (operador) { 
    case "*":  
      return multiplicar (x1 , x2);  
      break; 
    case "/": 
      return dividir (x1 ,x2); 
      break;  
    case "+":   
      return somar (x1 , x2);
      break;
    case "-": 
      return subtrair (x1 - x2);  
      break

    default: 
      return "OPERADOR INVALIDO";  














  }
}



